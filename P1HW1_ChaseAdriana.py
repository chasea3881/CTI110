# ---Calculating Exponents---

# What is the base value
base = int(input("Enter an integer as the base value: "))
exp = int(input("Enter an integer as the exponent: "))

# Calculate and print the result
result = base ** exp
print(str(base) + " raised to the power of " + str(exp) + " is " + str(result) + " !!")

# ---Addition and Subtraction---

# Get numbers from user
startinteger = int(input("Enter a starting integer: "))
Addinteger = int(input("Enter an integer to add: "))
Subtract = int(input("Enter an integer to subtract: "))

# Calculate and print the result
result = startinteger + Addinteger - Subtract
print(str(startinteger) + " + " + str(Addinteger) + " - " + str(Subtract) + " is equal to " + str(result))
