import turtle

win = turtle.Screen()
ac = turtle.Turtle()

i=0
while i < 4:
    ac.forward(100)
    ac.right(90)
    i = i+1
i=0

while i < 1:
    ac.left(180)
    ac.forward(150)
    i=i+1
i=0

while i<3:
    ac.forward(100)
    ac.right(120)
    i=i+1

win.mainloop()





